(function () {
  'use strict';

  angular
    .module('emailtemplates.admin')
    .controller('EmailtemplatesAdminListController', EmailtemplatesAdminListController);

  EmailtemplatesAdminListController.$inject = ['EmailtemplatesService', '$filter'];

  function EmailtemplatesAdminListController(EmailtemplatesService, $filter) {
    var vm = this;

    vm.buildPager = buildPager;
    vm.figureOutItemsToDisplay = figureOutItemsToDisplay;
    vm.pageChanged = pageChanged;
    EmailtemplatesService.query(function(data) {
      vm.emailtemplates = data;
      vm.buildPager();
    });


    function buildPager() {
      vm.pagedItems = [];
      vm.itemsPerPage = 15;
      vm.currentPage = 1;
      vm.figureOutItemsToDisplay();
    }

    function figureOutItemsToDisplay() {
      vm.filteredItems = $filter('filter')(vm.emailtemplates, {
        $: vm.search
      });
      vm.filterLength = vm.filteredItems.length;
      var begin = ((vm.currentPage - 1) * vm.itemsPerPage);
      var end = begin + vm.itemsPerPage;
      vm.pagedItems = vm.filteredItems.slice(begin, end);

    }

    function pageChanged() {
      vm.figureOutItemsToDisplay();
    }

  }
}());
